    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ProdutosBEAN produto = new ProdutosBEAN();
        produto.setTamanho(cmbTamanho.getSelectedItem() + "");
        produto.setDescricao(taDescricao.getText());
        produto.setNome(tfNome.getText());
        produto.setPrecoBase(Float.parseFloat(tfPreco.getText()));
        pControle.cadastrar(produto);
        this.preencheTabela();

        JOptionPane.showMessageDialog(null, "Produto CADASTRADO com sucesso");

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean retorno = pControle.remover(Integer.parseInt(lblCod.getText()));
        //se a variavel retorno for igual a true o usuario foi exluido
        if (retorno == true) {
            JOptionPane.showMessageDialog(null, "Produto EXCLUÍDO com sucesso");
            //solicita a atualização da tabela ou seja preenche ela toda novamente
          this.preencheTabela();
            //chama o método para limpar campos
            this.limparCampos();
        } else {
            //mensagem de erro
            JOptionPane.showMessageDialog(null, "ERRO na exclusão");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        ProdutosBEAN produto = new ProdutosBEAN();
        produto.setCod(Integer.parseInt(lblCod.getText()));
        produto.setTamanho(cmbTamanho.getSelectedItem() + "");
        produto.setDescricao(taDescricao.getText());
        produto.setNome(tfNome.getText());
        produto.setPrecoBase(Float.parseFloat(tfPreco.getText()));
        
        //chama o método de controle para editar
        boolean retorno = pControle.editar(produto);
        //se a variavel retorno for igual a true o usuario foi editado
        if (retorno == true) {
            JOptionPane.showMessageDialog(null, "Produto MODIFICADO com sucesso");
            //solicita a atualização da tabela ou seja preenche ela toda novamente
            this.preencheTabela();
            //chama o método para limpar campos
            this.limparCampos();
        } else {
            //mensagem de erro
            JOptionPane.showMessageDialog(null, "ERRO na EDIÇÃO");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

